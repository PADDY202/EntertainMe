
public class Character {
//name, string -> get & constructor
//handle -> get & constructor
	private String name,handle;
	public Character(String cname, String chandle)
	{
		name = cname;
		handle= chandle;
	}
	public String getName()
	{
		return name;
	}
	public String getHandle()
	{
		return handle;
	}
	
}
